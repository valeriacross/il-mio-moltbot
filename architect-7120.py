def hard_cap_prompt(text, limit=2000):
    """Tronca il prompt al limite di caratteri sul boundary dell'ultima riga completa."""
    if len(text) <= limit:
        return text
    truncated = text[:limit]
    last_newline = truncated.rfind('\n')
    if last_newline > limit * 0.7:
        return truncated[:last_newline]
    return truncated


